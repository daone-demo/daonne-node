async function hydrateAi(pool) {
  const tasks = await rows(pool, `SELECT * FROM generation_task`);
  const tasksWithResultsJson = new Set();
  for (const row of tasks) {
    const results = jsonValue(row.results_json, null);
    if (Array.isArray(results) && results.length > 0) {
      tasksWithResultsJson.add(stringId(row.id));
    }
    store.generationTasks.set(stringId(row.id), {
      id: stringId(row.id),
      userId: stringId(row.user_id),
      projectId: nullableId(row.project_id),
      nodeId: row.node_id,
      taskType: row.task_type,
      capabilityCode: row.capability_code,
      prompt: row.prompt || "",
      parameters: jsonValue(row.parameters_json, {}),
      referenceAssetIds: jsonValue(row.reference_asset_ids_json, []),
      idempotencyKey: row.idempotency_key,
      providerTaskId: row.provider_task_id,
      status: row.status,
      progress: number(row.progress),
      estimatedPoints: number(row.estimated_points),
      actualPoints: nullableNumber(row.actual_points),
      error: row.error_code || row.error_message ? { code: row.error_code, message: row.error_message } : null,
      results: Array.isArray(results) ? results : [],
      createdAt: isoDate(row.created_at),
      updatedAt: isoDate(row.updated_at)
    });
  }

  const results = await rows(pool, `SELECT * FROM generation_result ORDER BY task_id, sort_no`);
  for (const row of results) {
    const task = store.generationTasks.get(stringId(row.task_id));
    if (!task) continue;
    if (tasksWithResultsJson.has(task.id)) continue;
    const asset = store.assets.get(stringId(row.asset_id));
    task.results.push({
      assetId: stringId(row.asset_id),
      type: asset?.type || task.taskType
    });
  }
}

