export async function hydratePostgresBusinessStore(pool) {
  resetBusinessStore();
  await hydrateUsers(pool);
  await hydratePlans(pool);
  await hydrateModels(pool);
  await hydrateInspirations(pool);
  await hydrateProjects(pool);
  await hydrateAssets(pool);
  await hydrateAi(pool);
  await hydrateChat(pool);
  await hydrateWorkflows(pool);
  await hydrateBilling(pool);
  await hydrateAdminContent(pool);
  const shouldPersistSeeds = SEED_BACKED_KEYS.some((key) => store[key].size === 0);
  reseedStore();
  return shouldPersistSeeds;
}

function resetBusinessStore() {
  for (const key of BUSINESS_STORE_KEYS) {
    store[key].clear();
  }
}

