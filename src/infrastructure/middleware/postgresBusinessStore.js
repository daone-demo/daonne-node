async function rows(pool, sql, values = []) {
  const result = await pool.query(sql, values);
  return result.rows;
}

function stringId(value) {
  return String(value);
}

function nullableId(value) {
  return value === undefined || value === null || value === "" || Number(value) === 0 ? null : String(value);
}

function number(value) {
  return Number(value || 0);
}

function nullableNumber(value) {
  return value === undefined || value === null || value === "" ? null : Number(value);
}

function boolean(value) {
  return value === true || value === 1 || value === "1" || value === "true";
}

function isoDate(value) {
  return value ? new Date(value).toISOString() : null;
}

function jsonValue(value, fallback) {
  if (value === undefined || value === null || value === "") {
    return fallback;
  }
  if (typeof value === "object") {
    return value;
  }
  try {
    return JSON.parse(value);
  } catch {
    return fallback;
  }
}

function isOpenEnded(value) {
  return value && new Date(value).getFullYear() >= 2999;
}

function publicObjectUrl(objectKey) {
  const baseUrl = appConfig.storage.publicBaseUrl || "/api/mock-files";
  return `${baseUrl.replace(/\/$/, "")}/${String(objectKey).split("/").map(encodeURIComponent).join("/")}`;
}
